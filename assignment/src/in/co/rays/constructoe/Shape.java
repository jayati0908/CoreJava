package in.co.rays.constructoe;

public class Shape {
		
		private String Shape;
		private  int size;
		public static final float PI = 3.14F;
		public String getShape() {
			return Shape;
		}
		public void setShape(String shape) {
			Shape = shape;
		}
		public int getSize() {
			return size;
		}
		public void setSize(int size) {
			this.size = size;
		}
		public static float getPi() {
			return PI;
		}
		
	
}
