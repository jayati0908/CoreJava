package in.co.rays.collectionsframework;

public class Employee {
	private String name;
	private int age;
	private int sal;
	public Employee() {}
	
	public Employee(String name, int age, int sal) {

		this.name = name;
		this.age = age;
		this.sal = sal;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public int getSal() {
		return sal;
	}
	

}
